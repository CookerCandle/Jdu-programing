#include <stdio.h>

int main(void) {
    printf("[%f]\n", 1.123); //[1.123000]
    printf("[%10f]\n", 1.123); //[  1.123000]
    printf("[%-10f]\n", 1.123); //[1.123000  ]
    printf("[%010f]\n", 1.123); //[001.123000]
    printf("[%.2f]\n", 1.123); //[1.12]
    printf("[%.10f]\n", 1.123); //[1.1230000000]
    printf("[%7.2f]\n", 1.123); //[   1.12]
    printf("[%-7.2f]\n", 1.123); //[1.12   ]
    printf("[%07.2f]\n", 1.123); //[0001.12]

    return (0);
}

