#include <stdio.h>

int main(void) {
    printf("[%f]\n", 1.123); //    [1.123000]
    printf("[%4f]\n", 1.123); //    [1.123000]
    printf("[%-4f]\n", 1.123); //    [1.123000]
    printf("[%04f]\n", 1.123); //    [1.123000]
    printf("[%7.0f]\n", 1.123); //    [      1]
    printf("[%-7.0f]\n", 1.123); //    [1      ]
    printf("[%07.0f]\n", 1.123); //    [0000001]

    return (0);
}
